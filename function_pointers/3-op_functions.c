/**
 * op_add - add
 * @a: variable
 * @b: variable
 *
 * Return: c (result)
 */

int op_add(int a, int b)
{
	int c;

	c = a + b;
	return (c);
}
/**
 * op_sub - sub
 * @a: variable
 * @b: variable
 *
 * Return: c (result)
 */
int op_sub(int a, int b)
{
	int c;

	c = a - b;
	return (c);
}
/**
 * op_mul - mul
 * @a: variable
 * @b: variable
 *
 * Return: c (result)
 */
int op_mul(int a, int b)
{
	int c;

	c = a * b;
	return (c);
}
/**
 * op_div - div
 * @a: variable
 * @b: variable
 *
 * Return: c (result)
 */
int op_div(int a, int b)
{
	int c;

	c = a / b;
	return (c);
}
/**
 * op_mod - mod
 * @a: variable
 * @b: variable
 *
 * Return: c (result)
 */
int op_mod(int a, int b)
{
	int c;

	c = a % b;
	return (c);
}
